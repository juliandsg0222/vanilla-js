// What will be the result for these expressions?

window.alert(5 > 4); // Displays "true"
window.alert("apple" > "pineapple"); // Displays "false"
window.alert("2" > "12"); // Displays "true"
window.alert(undefined == null); // Displays "true"
window.alert(undefined === null); // Displays "false"
window.alert(null == "\n0\n"); // Displays "false"
window.alert(null === +"\n0\n"); // Displays "false"
